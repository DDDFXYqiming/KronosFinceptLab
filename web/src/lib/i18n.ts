export function normalizeLanguage(value: unknown): Language {
  return value === "en-US" ? "en-US" : DEFAULT_LANGUAGE;
}

export function t(language: Language, key: string): string {
  return messages[language][key] || messages["zh-CN"][key] || key;
}

export function languageName(language: Language): string {
  return language === "en-US" ? "English" : "中文";
}

export function outputLanguageName(language: Language): string {
  return language === "en-US" ? "English" : "Simplified Chinese";
}

export function getStoredLanguage(): Language {
  if (typeof window === "undefined") return DEFAULT_LANGUAGE;
  try {
    const raw = window.localStorage.getItem("kronos-preferences");
    return normalizeLanguage(raw ? JSON.parse(raw)?.language : null);
  } catch {
    return DEFAULT_LANGUAGE;
  }
}
